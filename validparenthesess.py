def isValid(s):
    c = []
    if s[0] in [')','}',']']:
        return False
    for i in range(len(s)):
        if s[i] in [')','}',']'] and len(c)==0:
            return False
        elif s[i] in ['(', '{', '[']:
            c.append(s[i])
        elif s[i] == ']' and c[-1] == '[':
            c.pop(-1)
        elif s[i] == ')' and c[-1] == '(':
            c.pop(-1)
        elif s[i] == '}' and c[-1] == '{':
            c.pop(-1)
        else:
            return False
    if len(c) != 0:
        return False
    else:
        return True

